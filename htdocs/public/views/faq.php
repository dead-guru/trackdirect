    <h2>2. Що таке APRS?</h2>
    <p>
        APRS (Автоматична Пакетна Система Звітування) - це цифрова система зв'язку, яка використовує пакетне радіо для передачі тактичної інформації в реальному часі. Мережа APRS використовується радіоаматорами по всьому світу.
    </p>
    <p>
        Інформацію, що передається через мережу APRS, включає координати, висоту, швидкість, напрямок, текстові повідомлення, сповіщення, оголошення, бюлетені та погодні дані.
    </p>

